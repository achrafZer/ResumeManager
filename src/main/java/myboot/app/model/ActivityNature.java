package myboot.app.model;

/**
 * Enumerates the possible natures or types of activities that can be associated with a CV.
 *
 * @author Achraf
 * @version 1.0
 */
public enum ActivityNature {

    /**
     * Represents a professional work experience.
     */
    PROFESSIONAL_EXPERIENCE,

    /**
     * Represents formal educational experiences such as schooling or training.
     */
    EDUCATION,

    /**
     * Represents leisure or personal activities not directly related to work or education.
     */
    LEISURE,

    /**
     * Represents projects, which can be of various kinds, including academic, personal, or professional.
     */
    PROJECT
}
